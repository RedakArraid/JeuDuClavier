import { useState, useEffect, useCallback, useRef } from 'react';
import { GameState, FallingWord, DifficultyLevel, GameConfig } from '../types/game';
import { getRandomWord, reverseWord } from '../data/words';

const GAME_CONFIG: Record<DifficultyLevel, GameConfig> = {
  easy: { wordSpeed: 0.6, spawnRate: 0, maxWords: 1, reverseWords: false },
  normal: { wordSpeed: 0.8, spawnRate: 0, maxWords: 1, reverseWords: false },
  hard: { wordSpeed: 1.0, spawnRate: 0, maxWords: 1, reverseWords: false },
  expert: { wordSpeed: 1.2, spawnRate: 0, maxWords: 1, reverseWords: true }
};

export const useGame = () => {
  const [gameState, setGameState] = useState<GameState>({
    isPlaying: false,
    isPaused: false,
    isGameOver: false,
    difficulty: 'easy',
    stats: {
      score: 0,
      wpm: 0,
      accuracy: 100,
      level: 1,
      wordsTyped: 0,
      errorsCount: 0,
      timeElapsed: 0
    },
    fallingWords: [],
    currentInput: '',
    activeWordId: null,
    gameStartTime: 0
  });

  const gameLoopRef = useRef<number>();
  const originalWordRef = useRef<string>('');
  const currentTypedRef = useRef<string>('');
  const shouldSpawnRef = useRef<boolean>(false);

  const createFallingWord = useCallback((difficulty: DifficultyLevel): FallingWord => {
    const config = GAME_CONFIG[difficulty];
    let word = getRandomWord(difficulty);
    
    if (config.reverseWords) {
      word = reverseWord(word);
    }

    originalWordRef.current = word;
    currentTypedRef.current = '';
    console.log('üéØ Cr√©ation nouveau mot:', word, 'Difficult√©:', difficulty);

    return {
      id: Math.random().toString(36).substring(2, 11),
      text: word,
      x: 50,
      y: 3,
      speed: config.wordSpeed,
      typed: '',
      isComplete: false,
      isActive: true
    };
  }, []);

  const updateWordPositions = useCallback(() => {
    setGameState(prev => {
      if (!prev.isPlaying || prev.isPaused) return prev;
      if (prev.fallingWords.length === 0) return prev;
      
      const currentWord = prev.fallingWords[0];
      const newY = currentWord.y + currentWord.speed;
      
      // Game Over si le mot tombe trop bas
      if (newY > 92) {
        console.log('üíÄ GAME OVER - Mot tomb√© trop bas:', currentWord.text, 'Y:', newY);
        return {
          ...prev,
          isPlaying: false,
          isGameOver: true,
          fallingWords: [],
          stats: {
            ...prev.stats,
            errorsCount: prev.stats.errorsCount + 1
          }
        };
      }
      
      const updatedWords = [{
        ...currentWord,
        y: newY
      }];
      
      const timeElapsed = prev.gameStartTime > 0 
        ? Math.round((Date.now() - prev.gameStartTime) / 1000)
        : 0;
      
      return {
        ...prev,
        fallingWords: updatedWords,
        stats: {
          ...prev.stats,
          timeElapsed
        }
      };
    });
  }, []);

  // Gestion d'une touche press√©e - VERSION TOTALEMENT CORRIG√âE
  const handleKeyPress = useCallback((key: string) => {
    console.log('‚å®Ô∏è handleKeyPress appel√© avec:', key);
    console.log('üìä currentTypedRef avant:', currentTypedRef.current);
    
    // V√©rifications pr√©liminaires
    if (gameState.fallingWords.length === 0) {
      console.log('‚ùå Pas de mot actuel √† taper');
      return;
    }
    
    const originalWord = originalWordRef.current;
    const currentTyped = currentTypedRef.current;
    const newTyped = currentTyped + key;
    
    console.log(`üéØ Test: "${newTyped}" vs mot original "${originalWord}"`);
    console.log(`üìù Tap√© jusqu'ici: "${currentTyped}" + "${key}" = "${newTyped}"`);
    
    // V√©rifier si la nouvelle saisie correspond au d√©but du mot
    if (originalWord.toLowerCase().startsWith(newTyped.toLowerCase())) {
      console.log('‚úÖ Lettre CORRECTE!');
      
      // Mettre √† jour la ref IMM√âDIATEMENT
      currentTypedRef.current = newTyped;
      const remainingText = originalWord.substring(newTyped.length);
      
      console.log(`üìù Reste √† taper: "${remainingText}"`);
      console.log('üìä currentTypedRef apr√®s update:', currentTypedRef.current);
      
      // Mot compl√®tement tap√©
      if (remainingText === '') {
        console.log('üéâ MOT COMPL√âT√â!', originalWord);
        
        // Reset pour le prochain mot
        currentTypedRef.current = '';
        originalWordRef.current = '';
        shouldSpawnRef.current = true;
        
        setGameState(prev => {
          const timeElapsed = prev.gameStartTime > 0 
            ? (Date.now() - prev.gameStartTime) / 1000
            : 1;
          
          const wordsTyped = prev.stats.wordsTyped + 1;
          const wpm = timeElapsed > 0 ? Math.round((wordsTyped / timeElapsed) * 60) : 0;
          
          // Bonus de position
          const currentWord = prev.fallingWords[0];
          let bonus = 0;
          if (currentWord.y < 30) bonus = 50;
          else if (currentWord.y < 60) bonus = 25;
          
          console.log('üéÅ Bonus position:', bonus, 'points (Y:', currentWord.y.toFixed(1), ')');
          
          return {
            ...prev,
            fallingWords: [],
            currentInput: '',
            stats: {
              ...prev.stats,
              score: prev.stats.score + originalWord.length * 10 + bonus,
              wordsTyped,
              wpm,
              accuracy: 100,
              level: Math.floor(wordsTyped / 10) + 1,
              timeElapsed: Math.round(timeElapsed)
            }
          };
        });
      } else {
        // Mettre √† jour le mot avec la partie restante
        setGameState(prev => {
          const currentWord = prev.fallingWords[0];
          const updatedWord = {
            ...currentWord,
            text: remainingText,
            typed: newTyped
          };
          
          console.log('üìã Mot mis √† jour - Affich√©:', remainingText, 'Tap√©:', newTyped);
          
          return {
            ...prev,
            fallingWords: [updatedWord],
            currentInput: newTyped
          };
        });
      }
    } else {
      // Lettre incorrecte
      console.log('‚ùå Lettre INCORRECTE!');
      console.log(`   Attendu: "${originalWord[newTyped.length - 1]}" (position ${newTyped.length - 1})`);
      console.log(`   Re√ßu: "${key}"`);
      console.log(`   Mot attendu: "${originalWord}"`);
      
      setGameState(prev => ({
        ...prev,
        stats: {
          ...prev.stats,
          errorsCount: prev.stats.errorsCount + 1,
          accuracy: Math.round(((prev.stats.wordsTyped * 5) / Math.max(1, prev.stats.wordsTyped * 5 + prev.stats.errorsCount + 1)) * 100)
        }
      }));
    }
  }, [gameState.fallingWords.length]); // Ajouter d√©pendance pour acc√©der √† l'√©tat actuel

  // Gestion de la touche Backspace - VERSION CORRIG√âE
  const handleBackspace = useCallback(() => {
    console.log('‚å´ handleBackspace appel√©');
    console.log('üìä Tap√© actuellement:', currentTypedRef.current);
    
    setGameState(prev => {
      if (prev.fallingWords.length === 0 || currentTypedRef.current === '') {
        console.log('‚ùå Rien √† effacer (pas de mot ou rien tap√©)');
        return prev;
      }
      
      // Retirer la derni√®re lettre tap√©e
      const newTyped = currentTypedRef.current.slice(0, -1);
      currentTypedRef.current = newTyped;
      
      const originalWord = originalWordRef.current;
      const remainingText = originalWord.substring(newTyped.length);
      
      // Mettre √† jour le mot affich√©
      const currentWord = prev.fallingWords[0];
      const updatedWord = {
        ...currentWord,
        text: remainingText,
        typed: newTyped
      };
      
      console.log(`üîô Apr√®s backspace: tap√©="${newTyped}", mot affich√©="${remainingText}"`);
      
      return {
        ...prev,
        fallingWords: [updatedWord],
        currentInput: newTyped
      };
    });
  }, []);

  const spawnNextWord = useCallback(() => {
    if (!shouldSpawnRef.current) {
      console.log('üö´ Spawn bloqu√© - shouldSpawn:', shouldSpawnRef.current);
      return;
    }
    
    console.log('üÜï Tentative de spawn d\'un nouveau mot...');
    
    setGameState(prev => {
      if (!prev.isPlaying || prev.isPaused) {
        console.log('üö´ Spawn annul√© - Jeu pas actif');
        return prev;
      }
      if (prev.fallingWords.length > 0) {
        console.log('üö´ Spawn annul√© - Il y a d√©j√† un mot');
        return prev;
      }
      
      const newWord = createFallingWord(prev.difficulty);
      console.log('‚úÖ Nouveau mot spawn√© avec succ√®s:', newWord.text);
      shouldSpawnRef.current = false;
      
      return {
        ...prev,
        fallingWords: [newWord],
        currentInput: ''
      };
    });
  }, [createFallingWord]);

  const startGame = useCallback((difficulty: DifficultyLevel) => {
    console.log('üöÄ D√âMARRAGE DU JEU - Mode Clavier Direct');
    console.log('üìä Difficult√©:', difficulty, 'Config:', GAME_CONFIG[difficulty]);
    
    if (gameLoopRef.current) {
      cancelAnimationFrame(gameLoopRef.current);
    }
    
    // Reset complet
    originalWordRef.current = '';
    currentTypedRef.current = '';
    shouldSpawnRef.current = true;
    
    console.log('‚úÖ Reset effectu√© - Premier mot sera spawn√©');
    
    setGameState({
      isPlaying: true,
      isPaused: false,
      isGameOver: false,
      difficulty,
      gameStartTime: Date.now(),
      fallingWords: [],
      currentInput: '',
      activeWordId: null,
      stats: {
        score: 0,
        wpm: 0,
        accuracy: 100,
        level: 1,
        wordsTyped: 0,
        errorsCount: 0,
        timeElapsed: 0
      }
    });
  }, []);

  const pauseGame = useCallback(() => {
    console.log('‚è∏Ô∏è Pause/Reprendre jeu');
    setGameState(prev => ({ ...prev, isPaused: !prev.isPaused }));
  }, []);

  const stopGame = useCallback(() => {
    console.log('‚èπÔ∏è Arr√™t du jeu');
    if (gameLoopRef.current) {
      cancelAnimationFrame(gameLoopRef.current);
    }
    shouldSpawnRef.current = false;
    currentTypedRef.current = '';
    
    setGameState(prev => ({
      ...prev,
      isPlaying: false,
      isPaused: false,
      isGameOver: true
    }));
  }, []);

  const resetGame = useCallback(() => {
    console.log('üîÑ Reset du jeu');
    if (gameLoopRef.current) {
      cancelAnimationFrame(gameLoopRef.current);
    }
    
    originalWordRef.current = '';
    currentTypedRef.current = '';
    shouldSpawnRef.current = false;
    
    setGameState(prev => ({
      ...prev,
      isPlaying: false,
      isPaused: false,
      isGameOver: false,
      fallingWords: [],
      currentInput: '',
      activeWordId: null,
      stats: {
        score: 0,
        wpm: 0,
        accuracy: 100,
        level: 1,
        wordsTyped: 0,
        errorsCount: 0,
        timeElapsed: 0
      }
    }));
  }, []);

  // Boucle de jeu pour le mouvement
  useEffect(() => {
    if (gameState.isPlaying && !gameState.isPaused) {
      const gameLoop = () => {
        updateWordPositions();
        if (gameState.isPlaying && !gameState.isPaused) {
          gameLoopRef.current = requestAnimationFrame(gameLoop);
        }
      };
      
      gameLoopRef.current = requestAnimationFrame(gameLoop);
    }

    return () => {
      if (gameLoopRef.current) {
        cancelAnimationFrame(gameLoopRef.current);
      }
    };
  }, [gameState.isPlaying, gameState.isPaused, updateWordPositions]);

  // Spawner des mots quand n√©cessaire
  useEffect(() => {
    if (gameState.isPlaying && !gameState.isPaused && shouldSpawnRef.current) {
      console.log('‚è∞ Timer pour spawner un mot dans 1 seconde...');
      const timer = setTimeout(() => {
        spawnNextWord();
      }, 1000);
      
      return () => {
        console.log('‚è∞ Timer de spawn annul√©');
        clearTimeout(timer);
      };
    }
  }, [gameState.isPlaying, gameState.isPaused, gameState.fallingWords.length, spawnNextWord]);

  // Log des changements d'√©tat pour debug
  useEffect(() => {
    console.log('üìä √âtat du jeu mis √† jour:', {
      isPlaying: gameState.isPlaying,
      isPaused: gameState.isPaused,
      isGameOver: gameState.isGameOver,
      wordsCount: gameState.fallingWords.length,
      currentInput: gameState.currentInput
    });
  }, [gameState.isPlaying, gameState.isPaused, gameState.isGameOver, gameState.fallingWords.length, gameState.currentInput]);

  return {
    gameState,
    startGame,
    pauseGame,
    stopGame,
    resetGame,
    handleKeyPress,
    handleBackspace
  };
};
